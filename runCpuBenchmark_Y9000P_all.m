% runCpuBenchmark_Y9000P_all Local Lenovo Y9000P benchmark using all cores.
%
% Runs smoke, quick, standard, and stress. Crazy is intentionally excluded
% for the local laptop. This restores all active logical processors before
% running, which is useful after a P-core-only run in the same MATLAB session.

cpuMode = setCpuBenchmarkCpuMode('all');

suite = runBenchmarkProfileSuite( ...
    'Profiles', {'smoke', 'quick', 'standard', 'stress'}, ...
    'Threads', cpuMode.recommendedThreads, ...
    'Repeats', 3, ...
    'SaveTag', 'y9000p_all', ...
    'ForceBuild', false);

