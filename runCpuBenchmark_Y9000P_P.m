% runCpuBenchmark_Y9000P_P Local Lenovo Y9000P benchmark using P-cores only.
%
% Runs smoke, quick, standard, and stress. Crazy is intentionally excluded
% for the local laptop. This mode requires Windows CPU Set EfficiencyClass
% support and is intended for Intel hybrid CPUs.

cpuMode = setCpuBenchmarkCpuMode('pcores');

suite = runBenchmarkProfileSuite( ...
    'Profiles', {'smoke', 'quick', 'standard', 'stress'}, ...
    'Threads', cpuMode.recommendedThreads, ...
    'Repeats', 3, ...
    'SaveTag', 'y9000p_intel_p', ...
    'ForceBuild', false);

