% runCpuBenchmark_Server_P Intel server benchmark using P-cores only.
%
% This mode requires Windows CPU Set EfficiencyClass support. It pins MATLAB
% to the highest-efficiency logical processors before running the benchmark.

cpuMode = setCpuBenchmarkCpuMode('pcores');

suite = runBenchmarkProfileSuite( ...
    'Profiles', {'smoke', 'quick', 'standard', 'stress', 'crazy'}, ...
    'Threads', cpuMode.recommendedThreads, ...
    'Repeats', 3, ...
    'SaveTag', 'server_32gb_intel_p', ...
    'ForceBuild', false);

